# Python3 program to multiply all values in a list
# This uses Numpy Prod funtion

import numpy 
list1 = [1, 2, 3] 
list2 = [3, 2, 4] 

# using numpy.prod() to get the multiplications 
result1 = numpy.prod(list1) 
result2 = numpy.prod(list2) 
print(result1) 
print(result2) 
